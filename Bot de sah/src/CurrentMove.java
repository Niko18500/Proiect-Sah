public class CurrentMove {

    private String time;

    private String otim;

    private String move;


    public String getTime() {
        return time;
    }

    public String getOtim() {
        return otim;
    }

    public String getMove() {
        return move;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public void setOtim(String otim) {
        this.otim = otim;
    }

    public void setMove(String move) {
        this.move = move;
    }
}
